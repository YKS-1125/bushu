# ============================================================
#  第 ② 步：多轮对话版 —— 让 AI 记住上文（DeepSeek 版）
#
#  核心思路（重点理解这句）：
#    大模型本身没有记忆，它只看你这一次发给它的 messages。
#    想让它"记住"前面说过的话，就得把【整段对话历史】每次都发回去。
#    所以我们用一个 messages 列表，不停往里追加，越滚越长。
#
#  图例：
#    🟢可变  = 你可以自由修改的地方（值 / 你的内容）
#    🔴固定  = 语法或结构，改了会报错，别动
# ============================================================

import os                     # 🔴固定
from openai import OpenAI     # 🔴固定
from dotenv import load_dotenv   # 🔴固定：导入 dotenv 的读取函数

# ------------------------------------------------------------
# 0. 读取 .env 文件，把里面的 key 加载进"环境变量"
#    这样就不用每次开终端都手敲 $env:... 了
# ------------------------------------------------------------
load_dotenv()                # 🔴固定：执行后，.env 里的内容就进了 os.environ

# ------------------------------------------------------------
# 1. 建立客户端（和之前一样）
# ------------------------------------------------------------
client = OpenAI(
    api_key=os.environ.get("DEEPSEEK_API_KEY"),   # 🟢可变：环境变量名
    base_url="https://api.deepseek.com",          # 🟢可变：服务商地址
)

# ------------------------------------------------------------
# 2. ★关键改动★：把 messages 放到循环【外面】
#    这样它不会每轮清空，而是一直保存全部对话历史。
#    一开始只放一条 system 人设。
# ------------------------------------------------------------
messages = [
    # 人设内容是 🟢可变
    {"role": "system", "content": "你是林小禾的个人网站助手，友好、简洁地回答访客的问题。"},
]

# ------------------------------------------------------------
# 3. 循环对话
# ------------------------------------------------------------
while True:                                # 🔴固定
    question = input("\n你想问（输入 exit 退出）：")   # 🟢可变：提示语
    if question.strip().lower() in ("exit", "quit"):   # 🟢可变：退出词
        print("再见！")
        break                              # 🔴固定

    # (1) 把用户这句话【追加】进历史列表
    #     .append(...) 是 🔴固定 的列表方法：往列表末尾加一条
    messages.append({"role": "user", "content": question})

    # (2) 把【整个历史列表】发过去 —— 这就是 AI 能记住上文的原因
    response = client.chat.completions.create(
        model="deepseek-chat",             # 🟢可变
        messages=messages,                 # 🔴关键：传的是整个列表，不是单句
        stream=True,                       # ★🔴新增：开启流式，让 AI “写一点发一点”
    )

    # 流式下，response 不再是“完整答案”，而是“一块块碎片(chunk)”，要用循环收
    # end="" 🔴固定：不自动换行；flush=True 🔴固定：立即显示到屏幕（流式必开）
    print("AI回答：", end="", flush=True)
    answer = ""                            # 🟢可变：把碎片拼成完整答案（历史要存完整句）
    for chunk in response:                 # 🔴固定：逐块接收
        delta = chunk.choices[0].delta.content   # 🔴固定：这一小块的新增文字（注意是 delta 不是 message）
        if delta:                          # 🔴固定：有内容才处理（有些块是空的）
            print(delta, end="", flush=True)     # 🔴固定：立即打印这一块，不换行
            answer += delta                # 🟢可变：边显示边拼进完整答案
    print()                                # 🔴固定：全部结束后补一个换行

    # (3) 把拼好的完整回答追加进历史，下一轮它才“记得”自己说过什么
    #     role 用 "assistant" 表示这是 AI 说的话
    messages.append({"role": "assistant", "content": answer})
